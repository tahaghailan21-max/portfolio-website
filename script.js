const i18nNodes = document.querySelectorAll("[data-i18n]");
const i18nAriaNodes = document.querySelectorAll("[data-i18n-aria]");

const EN = {};
i18nNodes.forEach((el) => {
  const key = el.dataset.i18n;
  if (!(key in EN)) EN[key] = el.innerHTML;
});
i18nAriaNodes.forEach((el) => {
  const key = el.dataset.i18nAria;
  if (!(key in EN)) EN[key] = el.getAttribute("aria-label");
});

const DICTS = { en: EN, fr: FR, es: ES };
const LANG_META = {
  en: { code: "EN", flag: "assets/icons/flags/en.png" },
  fr: { code: "FR", flag: "assets/icons/flags/fr.png" },
  es: { code: "ES", flag: "assets/icons/flags/es.png" }
};

const langMenu = document.getElementById("langMenu");
const langBtn = document.getElementById("langBtn");
const langFlag = document.getElementById("langFlag");
const langCode = document.getElementById("langCode");

function applyLang(lang) {
  const dict = DICTS[lang] || EN;
  i18nNodes.forEach((el) => {
    const value = dict[el.dataset.i18n];
    if (value !== undefined) el.innerHTML = value;
  });
  i18nAriaNodes.forEach((el) => {
    const value = dict[el.dataset.i18nAria];
    if (value !== undefined) el.setAttribute("aria-label", value);
  });
  document.documentElement.lang = lang;
  langFlag.src = LANG_META[lang].flag;
  langCode.textContent = LANG_META[lang].code;
  document.querySelectorAll("#langList [data-lang]").forEach((btn) => {
    btn.setAttribute("aria-selected", String(btn.dataset.lang === lang));
  });
  localStorage.setItem("lang", lang);
}

langBtn.addEventListener("click", () => {
  const open = langMenu.classList.toggle("open");
  langBtn.setAttribute("aria-expanded", String(open));
});
document.querySelectorAll("#langList [data-lang]").forEach((btn) => {
  btn.addEventListener("click", () => {
    applyLang(btn.dataset.lang);
    langMenu.classList.remove("open");
    langBtn.setAttribute("aria-expanded", "false");
  });
});
document.addEventListener("click", (e) => {
  if (!langMenu.contains(e.target)) {
    langMenu.classList.remove("open");
    langBtn.setAttribute("aria-expanded", "false");
  }
});

const savedLang = localStorage.getItem("lang");
if (savedLang && savedLang !== "en" && DICTS[savedLang]) applyLang(savedLang);

